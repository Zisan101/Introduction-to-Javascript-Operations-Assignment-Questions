// Explain the role of operators in JavaScript. Why are they essential in programming

// Operators in JavaScript are symbols that perform specific actions on operands. They enable the manipulation of data, making decisions, and controlling the flow of a program. Operators are essential for the following reasons:

// Arithmetic Operations: They allow calculations like addition (+), subtraction (-), multiplication (*), and division (/).

// let result = 5 + 3;  // 8

// Assignment: The assignment operator (=) is used to assign values to variables.
// let x = 10;

// Comparison: These operators (==, ===, >, <) compare values and return a boolean.
// console.log(5 > 3);  // true

// Logical Operations: Logical operators (&&, ||, !) are used for decision-making based on conditions.
// let check = (5 > 3 && 10 < 15);  // true
