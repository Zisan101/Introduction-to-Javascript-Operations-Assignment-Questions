// Discuss the precedence and associativity of operators in JavaScript. Why is understanding these concepts
// important?

// Operator precedence in JavaScript determines the order in which operators are evaluated in expressions. Operators with higher precedence are executed before those with lower precedence. For example, multiplication (*) has higher precedence than addition (+), so in the expression 5 + 2 * 3, the multiplication happens first, resulting in 5 + 6 = 11 [2].

// Associativity defines the order in which operators of the same precedence level are evaluated. Most operators in JavaScript, such as + and *, are left-associative, meaning they are evaluated from left to right. Some, like the assignment operator (=), are right-associative, meaning they are evaluated from right to left.

// Example of associativity:
// let x = 10, y = 5, z = 3;
// let result = x - y - z;  // Left-to-right, result is (10 - 5) - 3 = 2

