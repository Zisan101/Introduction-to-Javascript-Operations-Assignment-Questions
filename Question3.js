// Differentiate between unary, binary, and ternary operators in JavaScript. Give examples of each

// Unary Operator:

// Operates on a single operand.
// Example: The increment operator (++) increases a value by 1

// let x = 5;
// x++;  // x becomes 6

// Binary Operator:

// Operates on two operands.
// Example: The addition operator (+) adds two values

// let a = 5, b = 10;
// let sum = a + b;  // sum is 15

// Ternary Operator:

// Operates on three operands and is also called the conditional operator. It checks a condition and returns one of two values based on the result.
// let age = 18;
// let canVote = (age >= 18) ? "Yes" : "No";  // canVote is "Yes"
