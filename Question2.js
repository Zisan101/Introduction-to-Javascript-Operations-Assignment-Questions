// Describe the categorization of operators in JavaScript based on their functionality. Provide examples for
// each category

// Arithmetic Operators: Perform mathematical operations like addition, subtraction, multiplication, etc.
// let sum = 5 + 3;  // 8
// let product = 5 * 3;  // 15

// Assignment Operators: Assign values to variables.
// let x = 10;  // assigns 10 to x
// x += 5;  // x = x + 5, so x is 15

// Comparison Operators: Compare two values and return a boolean (true/false).
// console.log(5 > 3);  // true
// console.log(5 === "5");  // false (strict equality)

// Logical Operators: Combine conditions or expressions, returning boolean values.
// console.log(true && false);  // false
// console.log(true || false);  // true

// Conditional (Ternary) Operator: Returns a value based on a condition.
// let result = (5 > 3) ? "Yes" : "No";  // "Yes"




